export function ShareButtons({ url, title }: Props) {
  const [copied, setCopied] = useState(false);

  const handleCopy = async () => {
    try {
      await navigator.clipboard.writeText(url);
      setCopied(true);
      setTimeout(() => setCopied(false), 2000);
    } catch {
      // fallback: do nothing
    }
  };

  return (
    <div className="flex items-center gap-2 flex-wrap">
      <span className="text-xs text-gray-400">Compartir:</span>
      {SHARE_ACTIONS.map(({ key, label, color, getHref, svg }) => (
        <a
          key={key}
          href={getHref(url, title)}
          target="_blank"
          rel="noopener noreferrer"
          aria-label={label}
          title={label}
          className="w-8 h-8 rounded-full flex items-center justify-center text-white transition-transform hover:scale-110 hover:shadow-md flex-shrink-0"
          style={{ backgroundColor: color }}
        >
          {svg}
        </a>
      ))}
      <button
        onClick={handleCopy}
        aria-label="Copiar enlace"
        title="Copiar enlace"
        className="w-8 h-8 rounded-full flex items-center justify-center transition-transform hover:scale-110 hover:shadow-md flex-shrink-0 bg-gray-200 text-gray-600 hover:bg-gray-300"
      >
        {copied ? (
          <Check size={14} className="text-green-600" />
        ) : (
          <Link2 size={14} />
        )}
      </button>
      {copied && (
        <span className="text-xs text-green-600 font-medium">
          Enlace copiado
        </span>
      )}
    </div>
  );
}
